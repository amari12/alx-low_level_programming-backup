#include "3-calc.h"

/**
 *op_add - function to add/sum
 *@a: first int
 *@b: second int
 *Return: int (sum)
 */

int op_add(int a, int b)
{
return (a + b);
}

/**
 *op_sub - difference of a and b
 *@a: int
 *@b: int
 *Return: int
 */

int op_sub(int a, int b)
{
return (a - b);
}

/**
 *op_mul - multiply
 *@a: int
 *@b: int
 *Return: int
 */

int op_mul(int a, int b)
{
return (a * b);
}

/**
 *op_div - divide
 *@a: int
 *@b: int
 *Return: int
 */

int op_div(int a, int b)
{
return (a / b);
}

/**
 *op_mod - modulus/remainder
 *@a: int
 *@b: int
 *Return: int
 */

int op_mod(int a, int b)
{
return (a % b);
}
